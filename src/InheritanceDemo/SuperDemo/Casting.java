package InheritanceDemo.SuperDemo;

public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double) a / b;

        // if i want to store one type of value into another type
        // then write that type in round brackets in front of
        // the value to be stored. its called as type casting

        System.out.println("c="+c);
    }
}
